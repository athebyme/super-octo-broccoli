# -*- coding: utf-8 -*-
"""
Безопасная обработка ошибок для API-ответов.

Предотвращает утечку внутренних деталей (путей, SQL-схемы, стектрейсов)
в JSON-ответы клиенту. Детали логируются на сервере.
"""
import logging

logger = logging.getLogger(__name__)

# Сообщения, безопасные для показа пользователю (содержат ожидаемые ошибки)
_SAFE_PREFIXES = (
    'Товар не найден',
    'Категория не найден',
    'Поставщик не найден',
    'Недопустим',
    'Не указан',
    'Файл не найден',
    'Некорректн',
    'Обязательн',
    'не может быть пуст',
    'уже существует',
    'Превышен лимит',
    'Нет данных',
    'Доступ запрещён',
    'HTTP Error',
)


def safe_error_message(exc: Exception, context: str = '') -> str:
    """
    Возвращает безопасное сообщение об ошибке для клиента.

    Если ошибка выглядит как пользовательская (начинается с известного префикса),
    возвращает её текст. Иначе — возвращает generic-сообщение и логирует детали.
    """
    msg = str(exc)

    # Разрешаем показывать ожидаемые пользовательские ошибки
    for prefix in _SAFE_PREFIXES:
        if msg.startswith(prefix):
            return msg

    # Логируем полную ошибку на сервер
    log_prefix = f"[{context}] " if context else ""
    logger.error(f"{log_prefix}Внутренняя ошибка: {exc}", exc_info=True)

    return 'Внутренняя ошибка сервера'
